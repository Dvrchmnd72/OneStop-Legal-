    </div>
    <div class="legal-list">
      <div class="legal-item">
        <div class="legal-number">1</div>
        <p><strong>Independent Legal Advice:</strong> Both parties must receive independent legal advice from separate qualified lawyers before signing the agreement.</p>
      </div>
      <div class="legal-item">
        <div class="legal-number">2</div>
        <p><strong>Written Agreement:</strong> The BFA must be in writing and signed by both parties. Verbal agreements are not enforceable.</p>
      </div>
      <div class="legal-item">
        <div class="legal-number">3</div>
        <p><strong>Legal Advisor Statements:</strong> A signed statement from each party's lawyer confirming that independent advice was provided must be attached to the agreement.</p>
      </div>
      <div class="legal-item">
        <div class="legal-number">4</div>
        <p><strong>Full Financial Disclosure:</strong> Both parties must provide honest and complete disclosure of their financial circumstances. Non-disclosure can render the agreement invalid.</p>
      </div>
      <div class="legal-item">
        <div class="legal-number">5</div>
        <p><strong>Compliance with the Family Law Act:</strong> The agreement must comply with the <em>Family Law Act 1975 (Cth)</em> for married couples, or relevant state legislation for de facto couples.</p>
