    <div class="faq-list">
      <div class="faq-item">
        <div class="faq-question">
          <span>Can a BFA be overturned?</span>
          <span class="faq-toggle">+</span>
        </div>
        <div class="faq-answer">Yes, a court can set aside a BFA in limited circumstances — such as fraud, failure to disclose assets, duress, or if one party did not receive proper independent legal advice. That's why it's critical to have a qualified lawyer draft your agreement to minimise any risk of it being challenged.</div>
      </div>
      <div class="faq-item">
        <div class="faq-question">
          <span>Do both parties need separate lawyers?</span>
          <span class="faq-toggle">+</span>
        </div>
        <div class="faq-answer">Yes. Under the Family Law Act, both parties must obtain independent legal advice from their own separate lawyer. This is a mandatory legal requirement — without it, the BFA may not be enforceable.</div>
      </div>
      <div class="faq-item">
        <div class="faq-question">
          <span>How much does a BFA cost?</span>
          <span class="faq-toggle">+</span>
        </div>
        <div class="faq-answer">The cost depends on the complexity of your financial situation. At OneStop Legal, we offer transparent, competitive pricing and will provide a clear quote upfront after your initial consultation. Contact us for a free, no-obligation discussion.</div>
      </div>
      <div class="faq-item">
        <div class="faq-question">
          <span>Can a BFA be changed after it's signed?</span>
          <span class="faq-toggle">+</span>
        </div>
        <div class="faq-answer">Yes. A BFA can be varied or terminated by a new written agreement between both parties, provided both receive fresh independent legal advice. We can assist with updating your agreement as your circumstances change.</div>
      </div>
      <div class="faq-item">
        <div class="faq-question">
          <span>Does a BFA cover children's matters?</span>
          <span class="faq-toggle">+</span>
        </div>
        <div class="faq-answer">No. A BFA deals with financial and property matters only. Child custody, parenting arrangements, and child support are governed separately under the Family Law Act and cannot be included in a BFA.</div>
      </div>
      <div class="faq-item">
        <div class="faq-question">
          <span>Is a BFA the same as a prenup?</span>
          <span class="faq-toggle">+</span>
        </div>
        <div class="faq-answer">Essentially, yes. In Australia, what is commonly called a "prenup" or "prenuptial agreement" is formally known as a Binding Financial Agreement (BFA). However, BFAs can be made at any stage — before, during, or after a relationship — not just before marriage.</div>
