  'contract'      => '📋',
  'family'        => '👨‍👩‍👧',
  'financial'     => '💰',
);

function osl_get_cat_icon( $post_id, $cat_icons ) {
  $cats = get_the_category( $post_id );
  if ( $cats ) {
    foreach ( $cats as $cat ) {
      $slug = strtolower( $cat->slug );
      foreach ( $cat_icons as $keyword => $icon ) {
        if ( strpos( $slug, $keyword ) !== false ) return $icon;
      }
      $name = strtolower( $cat->name );
      foreach ( $cat_icons as $keyword => $icon ) {
        if ( strpos( $name, $keyword ) !== false ) return $icon;
      }
    }
  }
  // Check title as fallback
  $title = strtolower( get_the_title( $post_id ) );
  foreach ( $cat_icons as $keyword => $icon ) {
    if ( strpos( $title, $keyword ) !== false ) return $icon;
  }
  return '⚖️'; // default legal icon
}

