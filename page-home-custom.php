$cat_icons = array(
  'conveyancing'  => '🏠',
  'property'      => '🏠',
  'wills'         => '📜',
  'estate'        => '📜',
  'compensation'  => '⚖️',
  'litigation'    => '⚖️',
  'builder'       => '🔨',
  'construction'  => '🔨',
  'commercial'    => '💼',
